// display item functionality
function itemClicked(index) {
    console.log("hello", index)
    selectedItem = itemList.find(element => {
        return element.id === index
    });
    parent.document.getElementById('itemscontent').src = "./itemInfo.html";
}

// pagination functionality
var currentPage = 1;
function showrecords(index) {
    if (index === 0 && currentPage > 1) {
        currentPage -= 1;
    }
    if (index === 10 && currentPage > 3) {
        currentPage += 1;
    } else {
        currentPage = index;
    }

    switch (currentPage) {
        case 1:
            itemsList = itemList.slice(0, 6);
            break;
        case 2:
            itemsList = itemList.slice(6, 12);
            break;
        case 3:
            itemsList = itemList.slice(12, itemList.length);
            break;
        default:
            break;
    }
    createCards();
}

// Filter functinality
function onsearchText() {
    var searchText = document.getElementById('searchText').value;
    itemsList = itemList.filter(element => {
        if (element.category.indexOf(searchText) != -1) {
            return element.category.includes('searchText');
        }
    });

    createCards();

}

