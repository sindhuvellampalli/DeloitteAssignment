// generate itemList template
function createCards() {
    var items = '';
    if (itemsList.length > 0) {
        itemsList.forEach(element => {

            index = element.id;

            items += '<div class="col-sm-4 list" onclick="itemClicked(' + index + ')" ><div class="product-image-wrapper"><div class="single-products"><div class="productinfo text-center">';
            items += '<img  id=' + element.id + ' alt="" />';
            items += '<p class="item-name">' + element.collection + '</p>';
            items +=
                '<div class="product-price">   <label class="item-info">' + element.category + '</label><label class="price-info">' + element.price + '</label></div>';
            items += ' <div class="product-price"><div><span class="fa fa-star checked"></span><span class="fa fa-star checked"></span><span class="fa fa-star checked"></span><span class="fa fa-star checked"></span><span class="fa fa-star unchecked"></span></div>';
            items += '    <a class="btn btn-default add-to-cart"><i class="fa fa-shopping-cart"></i></a></div></div></div></div></div>';

        });
    }
    else {
        items = '<label>No records found</label>'
    }
    var itemParent = document.getElementById('parentCard');
    itemParent.innerHTML = items;
    itemsList.forEach(element => {
        var item = document.getElementById(element.id);
        item.src = element.displayImage;
    });
}

